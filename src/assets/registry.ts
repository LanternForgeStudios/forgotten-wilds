export type AssetCategory =
  | 'character'
  | 'enemy'
  | 'portrait'
  | 'tileset'
  | 'map'
  | 'ui'
  | 'icon'
  | 'background'
  | 'audio'
  | 'structure';

export type AssetStatus = 'placeholder' | 'final';

export interface AssetDimensions {
  width: number;
  height: number;
}

export interface AssetDefinition {
  id: string;
  category: AssetCategory;
  intendedUse: string;
  /** Path relative to /assets/ under public/, e.g. "sprites/characters/player.png". */
  filePath: string;
  /** null for asset types dimensions don't apply to (audio, Tiled maps). */
  dimensions: AssetDimensions | null;
  status: AssetStatus;
  notes: string;
}

