export function findAsset(id: string): AssetDefinition | undefined {
  return ASSET_REGISTRY.find((a) => a.id === id);
}

export function listAssetsByCategory(category: AssetCategory): AssetDefinition[] {
  return ASSET_REGISTRY.filter((a) => a.category === category);
}
